package br.com.Exercicio2;

public class Animal {
	
	private String nome;
	private String fazerAlgo;
	private String Emitirsom;
	private int idade;	
	
	
	public String getEmitirsom() {
		return Emitirsom;
	}
	public void setEmitirsom(String emitirsom) {
		Emitirsom = emitirsom;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(final String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getFazerAlgo() {
		return fazerAlgo;
	}
	public void setFazerAlgo(String fazerAlgo) {
		this.fazerAlgo = fazerAlgo;
	}
	

	
	
	
	
}
